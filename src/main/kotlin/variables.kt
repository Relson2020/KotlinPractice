fun main(args : Array<String>){
    // immutable variable
    val integer : Int = 21
    // mutable variable
    var string: String = "HEY"
    println("immutable variable : ${integer}  \nmutable variable : ${string}")
    // changing the value of mutable variable
    string = "HEY THERE"
    println("value of mutable variable after assigning the new value : ${string}")



}